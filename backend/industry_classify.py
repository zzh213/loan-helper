def classify_by_keyword(text: str) -> Tuple[str, int]:
    """关键词打分匹配。返回(类别, 命中分数);无命中返回("", 0)。"""
    if not text:
        return "", 0
    low = text.lower()
    best, best_score = "", 0
    for cat, kws in KEYWORD_MAP.items():
        score = 0
        for kw in kws:
            if kw.lower() in low:
                # 命中即计分,较长关键词权重更高
                score += 3 if len(kw) >= 3 else 2
        # 类别名本身出现直接强命中
        if cat in text:
            score += 4
        if score > best_score:
            best, best_score = cat, score
    return best, best_score


def _classify_by_llm(text: str) -> str:
    """调用大模型判定类别;失败或结果非法返回空串。"""
    options = "、".join(CANONICAL_INDUSTRIES)
    system = (
        "你是行业分类助手。用户会给出一个企业的经营内容或行业描述,"
        "你必须从给定的固定类别中,选出最贴切的一个。"
        "只输出类别名称本身,不要输出任何解释、标点或多余文字。"
    )
    prompt = (
        f"可选类别(只能选其一):{options}\n"
        f"企业经营/行业描述:{text}\n"
        f"请只回答一个最贴切的类别名称。"
    )
    ans = chatbot.complete(prompt, system=system, max_tokens=16)
    if not ans:
        return ""
    ans = ans.strip().strip("。.,、 \n\t")
    # 精确匹配
    if ans in CANONICAL_INDUSTRIES:
        return ans
    # 模型可能带多余字,做包含匹配
    for cat in CANONICAL_INDUSTRIES:
        if cat in ans:
            return cat
    return ""


def classify(text: str) -> Dict[str, str]:
    """识别自由文本所属的规范行业类别。

    返回 {category, label, method}:
      - category: 规范类别(下游逻辑使用)
      - label:    用户原始描述(用于展示/留存)
      - method:   'ai' | 'keyword' | 'default'
    """
    label = (text or "").strip()
    if not label:
        return {"category": "服务业", "label": "", "method": "default"}

    # 1) 大模型
    cat = _classify_by_llm(label)
    if cat:
        return {"category": cat, "label": label, "method": "ai"}

    # 2) 关键词回退
    kw_cat, score = classify_by_keyword(label)
    if kw_cat and score >= 2:
        return {"category": kw_cat, "label": label, "method": "keyword"}

    # 3) 兜底
    return {"category": "服务业", "label": label, "method": "default"}
