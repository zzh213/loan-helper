MAX_LEVEL = LEVELS[-1]["level"]


def level_info(coins: int) -> dict:
    """根据累计金币返回当前等级、称号,以及升级进度。"""
    current = LEVELS[0]
    for lv in LEVELS:
        if coins >= lv["min_coins"]:
            current = lv
        else:
            break
    nxt: Optional[dict] = None
    for lv in LEVELS:
        if lv["min_coins"] > coins:
            nxt = lv
            break
    if nxt:
        span = nxt["min_coins"] - current["min_coins"]
        gained = coins - current["min_coins"]
        progress = round(gained / span * 100) if span > 0 else 100
        coins_to_next = nxt["min_coins"] - coins
    else:
        progress = 100
        coins_to_next = 0
    return {
        "level": current["level"],
        "title": current["title"],
        "emoji": current["emoji"],
        "next_title": nxt["title"] if nxt else None,
        "next_level": nxt["level"] if nxt else None,
        "coins_to_next": coins_to_next,
        "progress": progress,
        "is_max": nxt is None,
    }


def list_levels() -> List[dict]:
    """返回所有关卡及其题目数量,供前端展示选择。"""
    out = []
    for lv in LEVELS:
        count = sum(1 for q in QUESTIONS if q["level"] == lv["level"])
        out.append({**lv, "question_count": count})
    return out


def _public(q: dict) -> dict:
    """对外暴露题目时隐藏答案与解析,防止作弊。"""
    return {
        "id": q["id"],
        "level": q["level"],
        "type": q["type"],
        "question": q["question"],
        "options": q["options"],
        "coins": q["coins"],
        "course": q.get("course"),
    }


def list_courses() -> List[dict]:
    """返回体系化课程及题目数量。"""
    return [{**c, "question_count": sum(1 for q in QUESTIONS if q.get("course") == c["key"])}
            for c in COURSES]


def get_questions(level: Optional[int] = None, count: int = 5, course: Optional[str] = None) -> List[dict]:
    """随机抽取题目(不含答案)。可按关卡 level 或课程 course 过滤。"""
    pool = [q for q in QUESTIONS
            if (level is None or q["level"] == level) and (course is None or q.get("course") == course)]
    random.shuffle(pool)
    if count > 0:
        pool = pool[:count]
    return [_public(q) for q in pool]


def get_question(qid: str) -> Optional[dict]:
    for q in QUESTIONS:
        if q["id"] == qid:
            return q
    return None


def check_answer(qid: str, choice: int) -> Optional[dict]:
    """校验答案,返回是否正确、正确项、解析、知识科普与本题金币。"""
    q = get_question(qid)
    if not q:
        return None
    correct = int(choice) == q["answer"]
    return {
        "question_id": qid,
        "correct": correct,
        "correct_index": q["answer"],
        "explanation": q["explanation"],
        "knowledge": q.get("knowledge", ""),
        "coins": q["coins"] if correct else 0,
    }
